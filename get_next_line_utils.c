#include "get_next_line.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;
	if (!s)
		return (0);
	i = 0;
	while (s[i])
		i++;
	return (i);
}

char	*ft_strchr(const char *s, int c)
{
	if (!s)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
			return((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}